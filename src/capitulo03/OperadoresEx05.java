package capitulo03;

public class OperadoresEx05 {
	public static void main(String[] args) {
		// OPERADORES INCREMENTAIS E DECREMENTAIS
		int x = 2, y = 2;		
		x++; // SOMA MAIS UM NA VARIAVEL X
		y--; // SUBTRAI UM DA VARIAVEL Y
		System.out.println("x = " + x + ", y = " + y);
		
		int num = 5;
		System.out.println(num);
		System.out.println(++num); // PRE INCREMENTAL
		System.out.println(num);
		System.out.println(num++); // POS INCREMENTAL
	}
}
