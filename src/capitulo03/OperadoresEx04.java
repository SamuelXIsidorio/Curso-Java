package capitulo03;

public class OperadoresEx04 {
	public static void main(String[] args) {
		// OPERADORES DE ATRIBUIÇÃO REDUZIDA
		int x = 10, y = 3;
		System.out.println(x);
		x+=2; //x = x + 2;
		System.out.println(x);
		
		System.out.println(x+=2);
		System.out.println(y-=2);
		System.out.println(x*=3);
		System.out.println(y/=2);
		
	}
}
